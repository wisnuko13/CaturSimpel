public class Bidak {
    private String nama;
    private String warna;
    private String kolom;
    private int baris;

    public String getNama() {
        return nama;
    }


    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    public String getKolom() {
        return kolom;
    }

    public void setKolom(String kolom) {
        this.kolom = kolom;
    }

    public int getBaris() {
        return baris;
    }

    public void setBaris(Integer baris) {
        this.baris = baris;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
}
