public class Util {
    public static int getIntChar(String s){
        int i = 0;
        switch (s){
            case "a":
                i = 0;
                break;
            case "b":
                i = 1;
                break;
            case "c":
                i = 2;
                break;
            case "d":
                i = 3;
                break;
            case "e":
                i = 4;
                break;
            case "f":
                i = 5;
                break;
            case "g":
                i = 6;
                break;
            case "h":
                i = 7;
                break;
        }
        return i;
    }
}
